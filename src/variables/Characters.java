package variables;

public class Characters {
	public static void main(String[] args) {
		char letter = 'a';
		System.out.println(letter);
		
		char value = 66;
		System.out.println(value);
		
		value = (char) (value + 1);
		System.out.println(value);
		
		String world = "Im studying java";
		System.out.println(world);
		
		world = world + 2020;
		System.out.println(world);
	}
}
